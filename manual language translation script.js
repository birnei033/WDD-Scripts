// get all text Nodes
// Find text nodes under the parent Element
function textNodesUnder(node){
    var all = [];
    for (node=node.firstChild;node;node=node.nextSibling){
      if (node.nodeType==3) all.push(node);
      else all = all.concat(textNodesUnder(node));
    }
    return all;
  }
// translate text Nodes
var translate = (language, area, inner = false)=>{
	if(inner == false){
		translation.translations.forEach((translate, i)=>{
			textNodesUnder(document.querySelector(area)).forEach(function(data, i){
				if(translate['English'].toLowerCase() == data.textContent.toLowerCase()){
	// 				console.log( translate[currentLanguage].toLowerCase() + ' | ' + data.textContent.toLowerCase());
					data.textContent = translate[language];
// 					currentLanguage = language;
				}
			});
		});
	}else{
		area = document.querySelector(area);
		translation.translations.forEach((translate, i)=>{
			
			if(translate['English'].toLowerCase() == area.innerText.toLowerCase()){
// 				console.log( translate[currentLanguage].toLowerCase() + ' | ' + area.innerText.toLowerCase());
				area.innerText = translate[language];
// 				currentLanguage = language;
			}
		});
	}
    
}
